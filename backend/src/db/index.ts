import pg from 'pg';
import dotenv from 'dotenv';
import fs from 'fs';
import path from 'path';

dotenv.config();

const { Pool } = pg;
const connectionString = process.env.DATABASE_URL;

const realPool = new Pool({
  connectionString,
  ssl: connectionString?.includes('supabase.co') || connectionString?.includes('supabase.com')
    ? { rejectUnauthorized: false }
    : false
});

export let useSQLite = false;
let sqliteDb: any = null;

// Mock Pool for SQLite compatibility
export const pool: any = {
  on: () => {},
  connect: async () => {
    if (useSQLite) {
      return {
        query: (text: string, params?: any[]) => query(text, params),
        release: () => {}
      };
    } else {
      return realPool.connect();
    }
  },
  end: async () => {
    if (!useSQLite) {
      await realPool.end();
    }
  }
};

// Check database connection and set up fallback
const initDB = async () => {
  try {
    // Attempt connection to PG
    const client = await realPool.connect();
    client.release();
    console.log('⚡ Connected to PostgreSQL Database successfully.');
  } catch (err) {
    console.warn('⚠️ Warning: Failed to connect to PostgreSQL database. Switching to local SQLite fallback...');
    useSQLite = true;
    
    try {
      // Bypassing TS compiler checks for node:sqlite by using eval dynamic import
      const sqliteModule = await eval("import('node:sqlite')") as any;
      const DatabaseSync = sqliteModule.DatabaseSync;
      
      // Resolve path to database file in backend root (using __dirname since output is CJS)
      const dbPath = path.resolve(__dirname, '../../assetflow.db');
      console.log(`📂 Initializing SQLite database at: ${dbPath}`);
      sqliteDb = new DatabaseSync(dbPath);
      
      // Check if schema needs creation
      const tableCheck = sqliteDb.prepare("SELECT name FROM sqlite_master WHERE type='table' AND name='employees'").all();
      if (tableCheck.length === 0) {
        console.log('⚙️ No existing tables found. Creating SQLite schema and seeding data...');
        await setupSqliteSchema();
      } else {
        console.log('⚡ Local SQLite database is ready.');
      }
    } catch (sqliteErr) {
      console.error('❌ Critical: Failed to load built-in node:sqlite module:', sqliteErr);
    }
  }
};

// Programmatic schema setup and translation for SQLite
const setupSqliteSchema = async () => {
  try {
    const schemaPath = path.resolve(__dirname, '../../schema.sql');
    if (!fs.existsSync(schemaPath)) {
      console.error(`❌ Schema file not found at: ${schemaPath}`);
      return;
    }
    
    const schemaText = fs.readFileSync(schemaPath, 'utf8');
    
    // Translate schema from PG syntax to SQLite syntax
    const sqliteQueries = schemaText
      .replace(/DROP TABLE IF EXISTS (\w+) CASCADE/gi, 'DROP TABLE IF EXISTS $1')
      .replace(/BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY/gi, 'INTEGER PRIMARY KEY AUTOINCREMENT')
      .replace(/BIGINT REFERENCES/gi, 'INTEGER REFERENCES')
      .replace(/BIGINT/gi, 'INTEGER')
      .replace(/TIMESTAMPTZ/gi, 'TEXT')
      .replace(/NUMERIC\(\d+,\s*\d+\)/gi, 'REAL')
      .replace(/NUMERIC/gi, 'REAL')
      .replace(/ON UPDATE CASCADE/gi, '')
      .replace(/DEFAULT NOW\(\)/gi, "DEFAULT (datetime('now'))");
      
    // Execute DDL statements (SQLite allows multiple queries via exec)
    sqliteDb.exec(sqliteQueries);
    console.log('✅ SQLite Schema created successfully.');
    
    // Run seeding
    await seedSqlite();
  } catch (error) {
    console.error('❌ Failed to construct SQLite schema:', error);
  }
};

