// Unified Query Handler with PostgreSQL/SQLite routing and translations
export const query = async (text: string, params: any[] = []) => {
  const start = Date.now();
  try {
    if (useSQLite) {
      // Translate numbered bind variables ($1, $2) to positional bindings (?)
      const sqliteSql = text
        .replace(/\$\d+/g, '?')
        .replace(/NOW\(\)/gi, "datetime('now')"); // translate NOW() to SQLite datetime('now')
      
      // Determine if statement returns rows
      const isQueryReturningRows = sqliteSql.toLowerCase().includes('select') || sqliteSql.toLowerCase().includes('returning');
      
      const stmt = sqliteDb.prepare(sqliteSql);
      
      // Map booleans to SQLite integers (1/0)
      const mappedParams = params.map(p => typeof p === 'boolean' ? (p ? 1 : 0) : p);
      
      if (isQueryReturningRows) {
        const rows = stmt.all(...mappedParams) as any[];
        
        // SQLite converts boolean columns to numbers. Map back to true/false for frontend fields
        const mappedRows = rows.map(row => {
          const newRow = { ...row };
          if ('shared' in newRow) {
            newRow.shared = !!newRow.shared;
          }
          if ('isRead' in newRow) {
            newRow.isRead = !!newRow.isRead;
          }
          if ('is_read' in newRow) {
            newRow.is_read = !!newRow.is_read;
          }
          return newRow;
        });
        
        return { rows: mappedRows, rowCount: mappedRows.length };
      } else {
        const res = stmt.run(...mappedParams) as any;
        return { rows: [], rowCount: res.changes };
      }
    } else {
      const res = await realPool.query(text, params);
      const duration = Date.now() - start;
      console.log(`🔍 PG Executed query [${duration}ms]:`, { text: text.trim().substring(0, 80) + '...', rowsCount: res.rowCount });
      return res;
    }
  } catch (error) {
    console.error('❌ Query execution failed:', { text, error });
    throw error;
  }
};

